// Inject theme styles
function injectThemeStyles() {
  const style = document.createElement('style');
  style.textContent = themeStyles;
  document.head.appendChild(style);
}

// Theme detection: own URL params > parent URL params (srcdoc iframe) > default light
function initTheme() {
  let themeParam = new URLSearchParams(window.location.search).get('theme');
  if (!themeParam) {
    try { themeParam = new URLSearchParams(window.parent.location.search).get('theme'); }
    catch (e) { /* cross-origin — ignore */ }
  }
  const theme = (themeParam === 'light' || themeParam === 'dark') ? themeParam : 'light';
  document.documentElement.classList.remove('light', 'dark');
  document.documentElement.classList.add(theme);
  // Set AG Grid theme mode attribute for native dark mode support
  document.documentElement.setAttribute('data-ag-theme-mode', theme);
  // Expose isDark globally for JavaScript access in templates
  window.isDark = theme === 'dark';
}

// Initialize
injectThemeStyles();
initTheme();

// Configure Tailwind v4 to use class-based dark mode (instead of prefers-color-scheme)
const twConfig = document.createElement('style');
twConfig.setAttribute('type', 'text/tailwindcss');
twConfig.textContent = '@custom-variant dark (&:where(.dark, .dark *));';
document.head.appendChild(twConfig);
